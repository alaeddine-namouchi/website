            <a class="card-title text-uppercase">
                Q: Comment se règlent les infractions?
            </a>
        </div>
        <div id="collapseTwo" class="card-body collapse  ml-3" data-parent="#accordion">
            <p>
                Comment se règlent les infractions ?<br />
                <b>(Référence : Décision n°369 du 15 août 2001- Chapitre 11/Article 27 )</b> <br />

                Est passible d'une amende fixée à vingt dinars (20D) en cas de paiement immédiat et à vingt cinq dinars (25 D) en cas de paiement différé , tout voyageur surpris dans l'une des situations irrégulières suivantes :<br />
            <ul class="list-group-item-faq">
                <li>
                    Absence de titre de transport à bord des moyens de transport et/ou à l'intérieur des stations du métro et du TGM.
                </li>
                <li>
                    Dépassement de section.
                </li>
                <li>
                    Utilisation irrégulière d'un ticket à tarif réduit.
                </li>
                <li>
                    Utilisation d'un titre de transport hors de la zone ou de la ligne déterminant la validité de ce titre.
                </li>
                <li>
                    Utilisation d'un abonnement scolaire en dehors de sa période de validité.
                </li>
                <li>
                    Utilisation d'un titre de transport faux ou falsifié.
                </li>
                <li>
                    Utilisation d'une vignette d'abonnement mensuel non accompagnée de la carte spéciale d'identité.
                </li>
                <li>
                    Utilisation d'une vignette d'abonnement mensuel portant numéro différent de celui de la carte spéciale d^'identité.
                </li>
                <li>
                    Utilisation d'un ticket ou d'une carte hebdomadaire non compostés.
                </li>
                <li>
                    Utilisation d'une carte d'abonnement annuel impersonnel sans présentation de l'attestation d'utilisation de cette carte.
                </li>
            </ul>
            En cas de récidive au cours de la même année, l'amende est majorée de cinq dinars (5D) par infraction commise .<br />
            En cas d'utilisation d'une carte d'abonnement scolaire , mensuel ou annuel dont la validité a expiré , le voyageur doit payer en sus de l'amende indiquée au paragraphe premier du présent article , un montant correspondant à l'utilisation de l'abonnement depuis la date d'expiration jusqu'à la date de constat de l'infraction .<br /> Toute personne dont la carte d'abonnement a été saisie chez une tierce personne , est passible de l'amende indiquée au paragraphe premier du présent article , si elle n'a pas avisé la TRANSTU par écrit de la perte de la dite carte et ce dans un délai d'une semaine.
            Le refus de paiement de la pénalité entraine des poursuites judiciaires.<br />
            </p>
